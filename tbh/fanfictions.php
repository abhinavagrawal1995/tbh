									<p>For those of you who haven't read one yet, a Fanfiction is when someone takes either the story or characters (or both) of a certain piece of work, whether it be a novel, tv show, movie, etc, and create their own story based on it. Sometimes people will take characters from one movie and put them in another, which is called a cross-over. A good place to start is This website or others like <a href="https://www.fanfiction.net">Fanfiction.net</a> and <a href="https://www.wattpad.com">Wattpad</a>.</p>
